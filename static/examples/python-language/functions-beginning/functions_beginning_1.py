#!/usr/bin/env python3

# Функция должна быть объявлена до вызова в программе, иначе произойдёт ошибка
def greet(name):  # Заголовок функции greet() с параметром name
    # Здесь начинается тело функции
    print(f'Привет, {name}!')
    # Здесь заканчивается тело функции


# Вызов функции greet() с аргументом name
greet('друг')
